package forWhile;

public class ForWhile {
    public static void main(String[] args) {

        // While
        int i = 0;
        while (i<10) {
            System.out.print(i+" ");
            i++;
        }

        System.out.println();

        // For
        for(int j=0;j<10;j++) {
            System.out.print(j+" ");
        }
    }
}
